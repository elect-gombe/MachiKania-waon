static
int makenote(int pitch,int len,int vero,const int8_t *wavtable){
  int i;

  for(i=0;i<8;i++){
    if(!(soundchannel[i].on))break;
  }
  if(i==8){
    return -1;
  }
  soundchannel[i].on = 1;
  soundchannel[i].th = 0;
  soundchannel[i].t = 0;
  soundchannel[i].pitch = pitch+12;
  soundchannel[i].len = len;
  soundchannel[i].vero = vero;
  soundchannel[i].wavtable = wavtable;

  return 0;
}

static
void channelprocess(channel_t *ch,uint8_t *buff){
  int i;
  int new;
  
  if(!(ch->on))return;
  if(ch->t+DTIME>ch->len){
    for(i=0;i<DTIME;i++,ch->t++){
      if(ch->t>ch->len){
	if(ch->vero==0){
	  ch->on=0;
	  break;
	}ch->vero--;
      }
      new = (ch->wavtable[(ch->th>>(32-9))]*ch->vero)>>9;
      buff[i] += new;
      ch->th += onkai[ch->pitch];
    }
  }else{
    for(i=0;i<DTIME;i++,ch->t++){
      new = (ch->wavtable[(ch->th>>(32-9))]*ch->vero)>>9;
      buff[i] += new;
      ch->th += onkai[ch->pitch];
    }
  }
}


