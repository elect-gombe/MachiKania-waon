#define CLRFLG() do{DCH0INTCLR = 0x000000FF;}while(0)
#define ISHALFDONE() (DCH0INT & 0x40)
#define ISALLDONE()  (DCH0INT & 0x80)

static int k=0;

#pragma interrupt dma0int IPL3SOFT vector 40
void dma0int(){
  // This function is called every 1/60 sec.
  IFS1CLR = _IFS1_DMA0IF_MASK;
  int i,j;
  uint8_t *b;
  b = ISHALFDONE() ? buff : buff+ 1024;
  CLRFLG();
  if(humen == NULL)return;
  for(i=0;i<1024;i++)b[i]=128;
  for(j=0;j<8;j++){
    if(humen[k].time < (int)(mtime/15)){
      if(humen[k].time == -1)break;
      makenote(humen[k].pitch,humen[k].length*15,humen[k].vero,kukei);
      k++;
      
    }
    mtime+=DTIME;
    for(i=0;i<sizeof(soundchannel)/sizeof(soundchannel[0]);i++){
      channelprocess(&soundchannel[i],b+j*128);
    }
  }
}

void init_music(){
  // Use Timer3 and OC4 for sound.
  RPB13R=5;        //Use RPB13 for OC4
  OC4R=0;
  OC4CON=0x000e;   // Timer3, toggle mode
  OC4CONSET=0x8000;// Start OC4
  PR3 = 256;
  T3CON=0x8000;    // Prescaller: 1:32 (1.8 MHz), not yet started

  // Timer4 is used to intterupt every 1/60 sec, just after NTSC view.
  T4CON=0x0000;    // Prescaller: 1:1
  PR4 = 57272720 / 16000;
  T4CONbits.ON = 1;

  DCH0CON  = 0x12;
  DCH0ECON = (19<<8)|(1<<4);
  DCH0SSA  = ((unsigned)buff) & 0x1FFFFFFF;
  DCH0DSA  = ((unsigned)&OC4RS) & 0x1FFFFFFF;
  DCH0SSIZ = 2048;
  DCH0DSIZ = 1;
  DCH0CSIZ = 1;
  DCH0INTSET   = 0x00C00000;
  DCH0INTCLR   = 0x003F00FF;
  DCH0CONSET   = 0x80;

  // DMA2 interruption, IPL3
  IPC10bits.DMA0IP=3;
  IPC10bits.DMA0IS=0;
  IFS1bits.DMA0IF=0;
  IEC1bits.DMA0IE=1;
  // Initializations for music/sound.
}

